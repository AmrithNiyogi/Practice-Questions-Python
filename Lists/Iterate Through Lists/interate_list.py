if __name__ == '__main__':
    countries = ["USA", "UK", "France", "Germany"]
    print("Printing the list of countries using a for loop")
    for country in countries:
        print(country)

    index = 0
    fruits = ["apple", "banana", "cherry"]
    print("Printing the list of fruits using a while loop")
    while index < len(fruits):
        print(fruits[index])
        index += 1
        
